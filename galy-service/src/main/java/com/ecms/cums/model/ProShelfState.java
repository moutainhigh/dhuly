package com.ecms.cums.model;

public class ProShelfState {
	private String eshopCode;
	private String shelfInfo;
	
	private String sign;
	public String getEshopCode() {
		return eshopCode;
	}
	public void setEshopCode(String eshopCode) {
		this.eshopCode = eshopCode;
	}
	public String getShelfInfo() {
		return shelfInfo;
	}
	public void setShelfInfo(String shelfInfo) {
		this.shelfInfo = shelfInfo;
	}
	public String getSign() {
		return sign;
	}
	public void setSign(String sign) {
		this.sign = sign;
	}
	
	
	

}
