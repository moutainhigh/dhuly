package com.ecms.cums.airportorderinfo;

public class AirportPassengerTypes {

	private String ageType;
	private String printPrice;
	private Integer insurance;
	private Integer insuranceCount;
	private Integer realPrice;
	private Integer constructionFee;
	private Integer fuelTax;
	private Integer allPrices;

	public String getAgeType() {
		return ageType;
	}
	public void setAgeType(String ageType) {
		this.ageType = ageType;
	}
	public String getPrintPrice() {
		return printPrice;
	}
	public void setPrintPrice(String printPrice) {
		this.printPrice = printPrice;
	}
	public Integer getInsurance() {
		return insurance;
	}
	public void setInsurance(Integer insurance) {
		this.insurance = insurance;
	}
	public Integer getInsuranceCount() {
		return insuranceCount;
	}
	public void setInsuranceCount(Integer insuranceCount) {
		this.insuranceCount = insuranceCount;
	}
	public Integer getRealPrice() {
		return realPrice;
	}
	public void setRealPrice(Integer realPrice) {
		this.realPrice = realPrice;
	}
	public Integer getConstructionFee() {
		return constructionFee;
	}
	public void setConstructionFee(Integer constructionFee) {
		this.constructionFee = constructionFee;
	}
	public Integer getFuelTax() {
		return fuelTax;
	}
	public void setFuelTax(Integer fuelTax) {
		this.fuelTax = fuelTax;
	}
	public Integer getAllPrices() {
		return allPrices;
	}
	public void setAllPrices(Integer allPrices) {
		this.allPrices = allPrices;
	}

}
