package com.ecms.cums.airportorderinfo;

public class AirportCashBackVo {

	private String cashBackTime;
	private Integer cashBackStatus;
	private Integer cashBackPriceSum;
	public String getCashBackTime() {
		return cashBackTime;
	}
	public void setCashBackTime(String cashBackTime) {
		this.cashBackTime = cashBackTime;
	}
	public Integer getCashBackStatus() {
		return cashBackStatus;
	}
	public void setCashBackStatus(Integer cashBackStatus) {
		this.cashBackStatus = cashBackStatus;
	}
	public Integer getCashBackPriceSum() {
		return cashBackPriceSum;
	}
	public void setCashBackPriceSum(Integer cashBackPriceSum) {
		this.cashBackPriceSum = cashBackPriceSum;
	}

}
